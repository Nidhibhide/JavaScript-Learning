//2.session storage -
//data is cleared when tab is closed
//use cases - session specific data
//size - 5MB , No server access

//3.Cookies -
//small pieces of data stored in browser and sent to server with every request .
//can have expiry time
//size limit 4KB
//Can be used by server and client
//used for authentication
//Yes server access
